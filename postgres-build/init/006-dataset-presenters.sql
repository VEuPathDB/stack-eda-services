CREATE SCHEMA APIDBTUNING;

CREATE TABLE APIDBTUNING.STUDYIDDATASETID
   (
    STUDY_STABLE_ID VARCHAR(200),
	DATASET_ID VARCHAR(15),
	DATASET_SHORT_DISPLAY_NAME VARCHAR(200)
   );

CREATE TABLE APIDBTUNING.DATASETPROPERTY
   (	DATASET_PROPERTY_ID BIGINT,
	DATASET_PRESENTER_ID VARCHAR(15),
	PROPERTY VARCHAR(255),
	VALUE VARCHAR(4000),
	 CONSTRAINT DATASETPROPERTY_PK PRIMARY KEY (DATASET_PROPERTY_ID)
   );
   
   CREATE TABLE APIDBTUNING.DATASETPRESENTER
      (	DATASET_PRESENTER_ID VARCHAR(15), 
   	NAME VARCHAR(200), 
   	PROJECT_ID VARCHAR(40), 
   	DATASET_NAME_PATTERN VARCHAR(200), 
   	DISPLAY_NAME VARCHAR(200), 
   	SHORT_DISPLAY_NAME VARCHAR(200), 
   	SHORT_ATTRIBUTION VARCHAR(200), 
   	SUMMARY TEXT, 
   	PROTOCOL VARCHAR(4000), 
   	DESCRIPTION TEXT, 
   	USAGE VARCHAR(4000), 
   	CAVEAT VARCHAR(4000), 
   	ACKNOWLEDGEMENT VARCHAR(4000), 
   	RELEASE_POLICY VARCHAR(4000), 
   	DISPLAY_CATEGORY VARCHAR(60), 
   	TYPE VARCHAR(100), 
   	SUBTYPE VARCHAR(100), 
   	CATEGORY VARCHAR(100), 
   	IS_SPECIES_SCOPE SMALLINT,
   	BUILD_NUMBER_INTRODUCED SMALLINT,
   	DATASET_SHA1_DIGEST VARCHAR(50), 
   	 CONSTRAINT DATASETPRESENTER_PK PRIMARY KEY (DATASET_PRESENTER_ID)
   	 );